	mux.HandleFunc("POST /dsh/captain/finance/cod-records/{recordId}/remit", protected.handleCaptainRemitCod)
	mux.HandleFunc("GET /dsh/captain/finance/commissions", protected.handleCaptainFinanceCommissions)
