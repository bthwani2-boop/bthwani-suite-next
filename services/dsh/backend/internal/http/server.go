	mux.HandleFunc("POST /dsh/captain/finance/payouts", protected.handleCaptainCreatePayout)
	mux.HandleFunc("GET /dsh/field/finance/commissions", protected.handleFieldFinanceCommissions)
	mux.HandleFunc("GET /dsh/field/finance/wallet", protected.handleFieldFinanceWallet)
	mux.HandleFunc("GET /dsh/field/finance/payouts", protected.handleFieldFinancePayouts)
	mux.HandleFunc("POST /dsh/field/finance/payouts", protected.handleFieldCreatePayout)
	mux.HandleFunc("GET /dsh/field/finance/payout-destinations", protected.handleFieldListPayoutDestinations)
	mux.HandleFunc("POST /dsh/field/finance/payout-destinations", protected.handleFieldCreatePayoutDestination)
	mux.HandleFunc("PATCH /dsh/field/finance/payout-destinations/{destinationId}", protected.handleFieldUpdatePayoutDestination)
	mux.HandleFunc("DELETE /dsh/field/finance/payout-destinations/{destinationId}", protected.handleFieldDeletePayoutDestination)

	// Support Tickets & Operational Escalation
	mux.HandleFunc("POST /dsh/client/support/tickets", protected.handleCreateSupportTicket)
	mux.HandleFunc("GET /dsh/client/support/tickets", protected.handleListClientSupportTickets)
	mux.HandleFunc("GET /dsh/client/support/tickets/{ticketId}", protected.handleGetClientSupportTicket)
	mux.HandleFunc("POST /dsh/client/support/tickets/{ticketId}/messages", protected.handleAddClientSupportMessage)
	mux.HandleFunc("GET /dsh/operator/support/tickets", protected.handleListOperatorSupportTickets)
	mux.HandleFunc("GET /dsh/operator/support/tickets/{ticketId}", protected.handleGetOperatorSupportTicket)
	mux.HandleFunc("PATCH /dsh/operator/support/tickets/{ticketId}", protected.handleUpdateOperatorSupportTicket)
	mux.HandleFunc("POST /dsh/operator/support/tickets/{ticketId}/messages", protected.handleAddOperatorSupportMessage)
	mux.HandleFunc("POST /dsh/operator/support/tickets/{ticketId}/escalate", protected.handleEscalateSupportTicket)
